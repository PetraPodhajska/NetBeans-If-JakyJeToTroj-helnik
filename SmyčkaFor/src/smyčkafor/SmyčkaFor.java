
package smyčkafor;


public class SmyčkaFor {

 
    public static void main(String[] args) {
   
     for (int i=1; i <10; i++) {
         System.out.print (i+"");
          }
         System.out.println();
         
         //násobilka 3
         for (int i=1; i <=10; i++) {
              System.out.print (i*3+" ");
         }
         System.out.println();
         
          for (int i=1; i <=10; i++) {
              System.out.print (i*1+" ");
         }
         System.out.println();
         
         for (int i=1; i <=10; i++) {
              System.out.print (i*2+" ");
         }
         System.out.println();
    }
    }
    

